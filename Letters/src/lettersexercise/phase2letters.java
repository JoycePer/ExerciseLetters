package lettersexercise;

public class phase2letters {

	public static void main(String[] args) {
		
		char[]ch= {'j','o','y','c','e'};
		
		for (int i=0;i<5;i++) {
		
			if(ch[i] == 'a' || ch[i] == 'e' || ch[i] == 'i' || ch[i] == 'o' || ch[i] == 'u' ) {
	            System.out.println(ch[i] + " is vowel");
			}
			
		     else {
				System.out.println(ch[i] + " is a consonant");
		    
	    }
	}
	}
}
		



